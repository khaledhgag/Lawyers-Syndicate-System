const buildMaps = (q) => {
  const query = encodeURIComponent(q);
  return {
    mapEmbed: `https://www.google.com/maps?q=${query}&output=embed`,
    mapLink: `https://maps.google.com/?q=${query}`,
  };
};

export function buildCourts() {
  const courts = [];
  let n = 0;
  const push = (c) => courts.push({ ...c, ...buildMaps(`${c.name} ${c.governorate}`), order: n++ });

  // 1) المحاكم العليا والوطنية
  push({ name: 'محكمة النقض', degree: 'النقض', governorate: 'القاهرة', address: 'دار القضاء العالي - وسط البلد - القاهرة' });
  push({ name: 'المحكمة الدستورية العليا', degree: 'دستورية', governorate: 'القاهرة', address: 'كورنيش النيل - المعادي - القاهرة' });
  push({ name: 'مجلس الدولة', degree: 'مجلس الدولة', governorate: 'الجيزة', address: 'مبنى مجلس الدولة - الدقي - الجيزة' });
  push({ name: 'المحكمة الإدارية العليا', degree: 'إدارية', governorate: 'القاهرة', address: 'مجلس الدولة - القاهرة' });

  // 2) محاكم الاستئناف الثمانية
  appealSeats.forEach((seat) =>
    push({
      name: `محكمة استئناف ${seat}`,
      degree: 'استئناف',
      governorate: Object.keys(governorateCapitals).find((g) => governorateCapitals[g] === seat) || seat,
      address: seat,
    })
  );

  // 3) المحافظات المفصّلة (ابتدائية + جزئية)
  Object.entries(detailed).forEach(([gov, { primary, partial }]) => {
    primary.forEach((name) =>
      push({ name: `محكمة ${name} الابتدائية`, degree: 'ابتدائية', governorate: gov, address: `${name} - محافظة ${gov}` })
    );
    partial.forEach((name) =>
      push({ name: `محكمة ${name} الجزئية`, degree: 'جزئية', governorate: gov, address: `${name} - محافظة ${gov}` })
    );
  });

  // 4) باقي المحافظات: محكمة ابتدائية واحدة بالعاصمة
  Object.entries(governorateCapitals).forEach(([gov, capital]) => {
    if (detailed[gov]) return; // already detailed above
    push({ name: `محكمة ${capital} الابتدائية`, degree: 'ابتدائية', governorate: gov, address: `${capital} - محافظة ${gov}` });
  });

  return courts;
}
